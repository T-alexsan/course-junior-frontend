/**
 * for
 * while
 * do while
 * */


// for (начальное значение; условие; шаг) {
//
// }

for (let i = 0; i < 11; i += 1) {
console.log(i);
}

// while (условие) {
//
// }

let y = 0;
while (y < 11) {
console.log(y);
y += 1;
}


// do while
//
// do {
//
// } while (условие)

let f = 0;
do {
    console.log(f);
    f += 1;
} while (f <= 5);